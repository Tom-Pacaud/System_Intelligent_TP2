public class Cellule {

    private int x;
    private int value;

    public Cellule (int x, int value){
        this.x = x;
        this.value = value;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }
}
